<template>
    <a :href="href" @click="scroll"><span></span>Scroll</a>
</template>

<script>
    import jquery from 'jquery';

    export default {
        props: ['href'],
        methods: {
            scroll (event) {
                event.preventDefault();
                jquery('html, body').animate({
                    scrollTop: jquery(this.href).offset().top - 60
                }, 750);
            }
        }
    }
</script>

